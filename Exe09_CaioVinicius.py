# some os DOIS primeiros e MULTIPLIQUE esse total pelo TERCEIRO.

n1 = int (input ("Insira o primeiro valor inteiro: "))
n2 = int (input("Insira o segundo valor inteiro: "))
n3 = int (input("Insira o terceiro valor inteiro: "))

resultado = (n1 + n2) * n3

print("O total é: ", resultado)